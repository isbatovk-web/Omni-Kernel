//! # 🦀 Calc Module — Example Omni-Kernel Guest (Rust → WASM)
//!
//! This is a high-performance calculation module that will be compiled
//! to WebAssembly and loaded into the Omni-Kernel runtime.
//!
//! ## Compilation
//!
//! ```bash
//! # Add the WASM target
//! rustup target add wasm32-wasip1
//!
//! # Build the module
//! cargo build --target wasm32-wasip1 --release
//! ```
//!
//! ## Exported Functions
