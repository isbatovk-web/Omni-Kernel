//! - `add(a: i32, b: i32) -> i32`
//! - `multiply(a: i32, b: i32) -> i32`
//! - `fibonacci(n: i32) -> i64`
//! - `matrix_dot_product(ptr: *const f64, len: i32) -> f64`

