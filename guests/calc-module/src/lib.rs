/// Add two integers.
///
/// This is the simplest possible bridge function. When called via
/// `bridge_call("calc-module", "add", args_handle)`:
/// - args_handle contains 8 bytes: [a: i32 LE, b: i32 LE]
/// - Returns 4 bytes: [result: i32 LE]
#[no_mangle]
pub extern "C" fn add(a: i32, b: i32) -> i32 {
    a + b
}

/// Multiply two integers.
#[no_mangle]
pub extern "C" fn multiply(a: i32, b: i32) -> i32 {
    a * b
}

/// Compute the nth Fibonacci number iteratively.
///
/// This demonstrates a high-performance computation path — when this
/// module runs as WASM inside Omni-Kernel, it gets JIT-compiled to
/// native machine code by Wasmtime's Cranelift backend.
#[no_mangle]
pub extern "C" fn fibonacci(n: i32) -> i64 {
    if n <= 0 {
        return 0;
    }
    if n == 1 {
        return 1;
    }

    let mut a: i64 = 0;
    let mut b: i64 = 1;

    for _ in 2..=n {
        let temp = a + b;
        a = b;
        b = temp;
    }

    b
}

/// Compute the dot product of two vectors stored contiguously in memory.
///
/// This demonstrates how WASM modules can work with shared memory
/// efficiently — the vectors are stored in the Universal Memory Pool
/// and accessed via pointers, avoiding any serialization.
///
/// Layout: [vec_a: f64 × len, vec_b: f64 × len]
/// Total bytes: len * 8 * 2
#[no_mangle]
pub extern "C" fn dot_product(data_ptr: *const f64, len: i32) -> f64 {
    let len = len as usize;

    // Safety: In the Omni-Kernel environment, these pointers reference
    // validated regions of the Universal Memory Pool.
    unsafe {
        let vec_a = std::slice::from_raw_parts(data_ptr, len);
        let vec_b = std::slice::from_raw_parts(data_ptr.add(len), len);

        let mut sum: f64 = 0.0;
        for i in 0..len {
            sum += vec_a[i] * vec_b[i];
        }
        sum
    }
}

/// Matrix multiplication for square matrices (NxN).
///
/// Demonstrates compute-intensive operations that benefit from
/// WASM's near-native performance via Cranelift JIT compilation.
///
/// Layout: [matrix_a: f64 × n×n, matrix_b: f64 × n×n]
/// Result is written to a new shared memory block.
#[no_mangle]
pub extern "C" fn matrix_multiply(data_ptr: *const f64, n: i32) -> *const f64 {
    let n = n as usize;

    unsafe {
        let a = std::slice::from_raw_parts(data_ptr, n * n);
        let b = std::slice::from_raw_parts(data_ptr.add(n * n), n * n);

        // Allocate result via the bridge
        let result_size = (n * n * std::mem::size_of::<f64>()) as i32;
        let result_handle = bridge_alloc(result_size);

        // Create a local buffer for the result
        let mut result = vec![0.0f64; n * n];

        for i in 0..n {
            for j in 0..n {
                let mut sum = 0.0f64;
                for k in 0..n {
                    sum += a[i * n + k] * b[k * n + j];
                }
                result[i * n + j] = sum;
            }
        }

        // Write result to shared memory pool
        bridge_write(
            result_handle,
            0,
            result.as_ptr() as *const u8,
            result_size,
        );

        result.as_ptr()
    }
}

